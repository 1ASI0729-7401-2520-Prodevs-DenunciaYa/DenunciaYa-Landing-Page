    let currentLang = "EN";
function setLang(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });
  currentLang = lang;

  // Cambia el texto de TODOS los botones de idioma
  document.querySelectorAll(".langBtn").forEach(btn => {
    btn.textContent = lang === "EN" ? "EN" : "ES";
  });
}

document.querySelectorAll(".langBtn").forEach(btn => {
  btn.addEventListener("click", function() {
    setLang(currentLang === "ES" ? "EN" : "ES");
  });
});

//Hamburguesa menú móvil mejorado
const menuBtn = document.getElementById('menuBtn');
const mainNav = document.getElementById('mainNav');

// Agrega el header del menú solo en móvil
function updateMenuHeader() {
  if (window.innerWidth <= 767) {
    const menuHeader = mainNav.querySelector('.menu-header');
    if (menuHeader) menuHeader.style.display = 'flex';
  } else {
    const menuHeader = mainNav.querySelector('.menu-header');
    if (menuHeader) menuHeader.style.display = 'none';
    mainNav.classList.remove('open');
  }
}
window.addEventListener('resize', updateMenuHeader);
document.addEventListener('DOMContentLoaded', updateMenuHeader);

menuBtn.addEventListener('click', () => {
  mainNav.classList.add('open');
  updateMenuHeader();
});
const closeMenuBtn = document.getElementById('closeMenuBtn');
if (closeMenuBtn) {
  closeMenuBtn.addEventListener('click', () => {
    mainNav.classList.remove('open');
  });
}

// Cierra menú al hacer click fuera
document.addEventListener('click', function(e) {
  if (window.innerWidth <= 767) {
    if (!mainNav.contains(e.target) && e.target !== menuBtn) {
      mainNav.classList.remove('open');
    }
  }
});
